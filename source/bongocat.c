#define TAP_DURATION_MS 200
#define IDLE_TIMEOUT_MS 1200
#define DEBOUNCE_TIME_MS 100
//#define ANIMATION_DURATION_MS 300  // Adjusted for 4 frames

void bongocat_init(ssd1306_t *disp) {
    // Show idle frame initially
    ssd1306_clear(disp);
    ssd1306_bmp_show_image(disp, bongocat_idle, 512);
    ssd1306_show(disp);
}


void trigger_bongocat_tap() {
    uint32_t current_time = board_millis();

    if (current_time - last_tap_time < DEBOUNCE_TIME_MS) {
        return;  // Ignore this trigger due to debounce
    }
    
    is_tapping = true;
    tap_start_time = current_time;
    last_activity_time = current_time;
    last_tap_time = current_time;

    is_left_tap = !is_left_tap;  // Alternate between left and right taps
}

void update_bongocat_animation(ssd1306_t *disp) {
    const uint32_t interval_ms = 33;
    static uint32_t start_ms = 0;
    uint32_t current_time = board_millis();
    
    if (current_time - start_ms < interval_ms) return; // not enough time
    start_ms += interval_ms;

    
    ssd1306_clear(disp);
    if (!is_tapping) {
        if (current_time - last_activity_time < IDLE_TIMEOUT_MS) {
            // Still in active period, show paws up
            ssd1306_bmp_show_image(disp, bongocat_up, 512);
        } else {
            // Idle timeout reached, show paws down
            ssd1306_bmp_show_image(disp, bongocat_idle, 512);
        }
    } else {
        uint32_t elapsed_time = current_time - tap_start_time;
        
        if (elapsed_time < TAP_DURATION_MS) {
            if (is_left_tap) {
                ssd1306_bmp_show_image(disp, bongocat_left_tap, 512);  // Left paw tap
            } else {
                ssd1306_bmp_show_image(disp, bongocat_right_tap, 512);  // Right paw tap
            }
        } else {
            is_tapping = false;
            ssd1306_bmp_show_image(disp, bongocat_up, 512);  // Return to paws up after tap
        }
    }
    ssd1306_show(disp);
}

